    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "Select * from login where Username='"+txtuser.getText()+"' and Password='"+String.valueOf(txtpass.getPassword())+"'";
        try{
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                if(txtuser.getText().equals(rs.getString("Username"))&&String.valueOf(txtpass.getPassword()).equals(rs.getString("Password"))){
                    UserLogin.setUserLogin(rs.getString("Username"));
                    JOptionPane.showMessageDialog(this, "Login Sukses \n    Selamat Datang");
                    new Menu().setVisible(true);
                    this.dispose();                                        
                }else{
                    JOptionPane.showMessageDialog(this, "Username atau Password Salah", "Cannot Login", JOptionPane.ERROR_MESSAGE);
                }
            }
        }catch(SQLException ex){
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

